#include "libft.h"

int			ft_faequal(float nb)
{
	if (nb < 0)
	{
		if (nb * -1 < 0.00005)
			return (1);
		else
			return (0);
	}
	if (nb < 0.00005)
		return (1);
	return (0);
}
